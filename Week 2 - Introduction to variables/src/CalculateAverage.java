
public class CalculateAverage {

	public static void main(String[] args) {
		int number1 = 50;
		int number2 = 40;
		int number3 = 10; 
		int number4 = 100;
		int number5 = 300;

		double sum = number1 + number2 + number3 + number4 + number5;                                   
		double average = sum / 5;
		System.out.println("The average is: " + average);
	}
}